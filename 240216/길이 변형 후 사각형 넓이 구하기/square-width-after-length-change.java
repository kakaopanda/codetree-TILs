import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int width = sc.nextInt() + 8;
        int length = sc.nextInt() * 3;

        System.out.println(width);
        System.out.println(length);
        System.out.println(width * length);
    }
}